"""
WAP All the basic data type (int,float,string,tuple,dict,and set)
This program demonstrates the basic data types in Python.
"""

my_integer = 100
print(f"Integer Value: {my_integer}, Type: {type(my_integer)}")

my_float = 3.14
print(f"Float Value:   {my_float}, Type: {type(my_float)}")


my_string = "Hello, World!"
print(f"String Value:  '{my_string}', Type: {type(my_string)}")


my_tuple = (1, "apple", 2.5, 1)
print(f"Tuple Value:   {my_tuple}, Type: {type(my_tuple)}")


my_dict = {"name": "Ankit", "age": 25}
print(f"Dict Value:    {my_dict}, Type: {type(my_dict)}")






my_set = {1, 2, 3, 4, 4, 5}  
print(f"Set Value:     {my_set}, Type: {type(my_set)}")